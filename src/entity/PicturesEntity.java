package entity;

public class PicturesEntity {
    private String tel;
    private String picYear;
    private String picPathName;

    public String getTel() {
        return tel;
    }

    public void setTel(String tel) {
        this.tel = tel;
    }

    public String getPicYear() {
        return picYear;
    }

    public void setPicYear(String picYear) {
        this.picYear = picYear;
    }

    public String getPicPathName() {
        return picPathName;
    }

    public void setPicPathName(String picPathName) {
        this.picPathName = picPathName;
    }
}
